while (line = readline()) {
  if (line === 'end') {
    results.push('end');
    continue;
  }
  
  const h1 = line[0]; // hours first digit
  const h2 = line[1]; // hours 2nd digit
  const m1 = line[3]; // minutes first digit
  const m2 = line[4]; // minutes second digit
  
  for (let i = 0; i < 7; i += 1) {
    const outputLine = map[h1][i]
      .concat([' ', ' '])
      .concat(map[h2][i])
      .concat([' ', ' ', ' ', ' ', ' '])
      .concat(map[m1][i])
      .concat([' ', ' '])
      .concat(map[m2][i]);
    
    if (i === 2 || i === 4) {
      outputLine[14] = 'o';
    }
    
    results.push(outputLine.join(''));
    
    // push two empty lines at the end
    if (i === 6) {
      results.push([]);
      results.push([]);
    }
  }
}

print(results.join('\n'));
